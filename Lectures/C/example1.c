#include<stdio.h>
int main()
{
    // declaration with definition
    int defined_var;
   printf("Defined_var: %d\n", defined_var);
 
    // initialization
    defined_var = 12;
 
   // declaration + definition + initialization
    int ini_var = 25;
     printf("Value of defined_var after initialization: %d\n", defined_var);
    printf("Value of ini_var: %d", ini_var);
	
	 
   return 0;
}
